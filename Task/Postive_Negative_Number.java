public class Postive_Negative_Number 
{
	public static void main(String[] args)
	{
		 int number = -100;

	        if (number < 0)
	            System.out.println( " is a negative number.");

	        else if ( number > 0)
	            System.out.println( " is a positive number.");

	        else
	            System.out.println( " is Zero");
	}

}
